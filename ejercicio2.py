class Punto:
    def __init__(self, x, y):
        self.x = x
        self.y = y

punto1 = Punto(3, 4)
punto2 = Punto(-2, 7)

print("Punto 1 - Coordenada X:", punto1.x)
print("Punto 1 - Coordenada Y:", punto1.y)

print("Punto 2 - Coordenada X:", punto2.x)
print("Punto 2 - Coordenada Y:", punto2.y)
